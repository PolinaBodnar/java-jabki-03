public class Main {
    public static void main(String[] args) {
        // Четное или не четное

        int i;
        for (i = 0; i <= 10; i++)
            if (i % 2 == 0) {
                System.out.println(i + " - Число четное");
            } else {
                System.out.println(i + " - Число нечетное");
            }


    }
}